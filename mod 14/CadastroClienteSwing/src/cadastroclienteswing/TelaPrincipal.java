    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        String nome = txtNome.getText();
        String cpf = txtCPF.getText();
        String telefone = txtTelefone.getText();
        String endereco = txtEndereco.getText();
        String numero = txtNumero.getText();
        String cidade = txtCidade.getText();
        String estado = txtEstado.getText();
        
        if(!isCamposValidos(nome, cpf, telefone, endereco, numero, cidade, estado)){
            JOptionPane.showMessageDialog(this, "Existem campos a serem preenchidos. Verifique e tente novamente", "Erro", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        
        Cliente cliente = new Cliente(nome, cpf, telefone, endereco, numero, cidade, estado);
        Boolean isCadastrado = iClienteDAO.cadastrar(cliente);
        
        if (isCadastrado) {
            model.addRow(new Object[]{cliente.getNome(), cliente.getCpf(), cliente.getTelefone(), cliente.getEndereco()});
            limparCampos();
        }
        else {
            JOptionPane.showMessageDialog(null, "Cliente já se encontra cadastrado!", "Erro", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void tbClientesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbClientesMouseClicked
        int linhaSelecionada = tbClientes.getSelectedRow();
        String cpf = (String) tbClientes.getValueAt(linhaSelecionada, 1);
        
        Cliente cliente = iClienteDAO.consultar(cpf);
        
        txtNome.setText(cliente.getNome());
        txtCPF.setText(cliente.getCpf());
        txtTelefone.setText(String.valueOf(cliente.getTelefone()));
        txtEndereco.setText(cliente.getEndereco());
        txtNumero.setText(String.valueOf(cliente.getNumero()));
        txtCidade.setText(cliente.getCidade());
        txtEstado.setText(cliente.getEstado());
        
    }//GEN-LAST:event_tbClientesMouseClicked

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        int linhaSelecionada = tbClientes.getSelectedRow();
        
        if(linhaSelecionada >= 0){
            int result = JOptionPane.showConfirmDialog(this,
                    "Deseja realmente excluir este cliente?",
                    "Atenção",
                    JOptionPane.YES_NO_OPTION,
                    JOptionPane.QUESTION_MESSAGE);

            if (result == JOptionPane.YES_OPTION) {
                String cpf = (String) tbClientes.getValueAt(linhaSelecionada, 1);
                iClienteDAO.excluir(cpf);
                
                model.removeRow(linhaSelecionada);
                
                JOptionPane.showMessageDialog(null,
                        "Cliente excluído com sucesso!",
                        "Excluir",
                        JOptionPane.INFORMATION_MESSAGE);
                
                limparCampos();
            }
        }
        else {
                JOptionPane.showMessageDialog(null,
                        "Nenhum cliente selecionado!",
                        "Excluir",
                        JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

