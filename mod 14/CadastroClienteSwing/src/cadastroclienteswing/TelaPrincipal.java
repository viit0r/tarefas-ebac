    private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed
        int linhaSelecionada = tbClientes.getSelectedRow();
        
        if(linhaSelecionada >= 0){
            String cpf = (String) tbClientes.getValueAt(linhaSelecionada, 1);
            String nome = txtNome.getText();
            String telefone = txtTelefone.getText();
            String endereco = txtEndereco.getText();
            String numero = txtNumero.getText();
            String cidade = txtCidade.getText();
            String estado = txtEstado.getText();
            
            if(!isCamposValidos(nome, cpf, telefone, endereco, numero, cidade, estado)){
                JOptionPane.showMessageDialog(this, "Existem campos a serem preenchidos. Verifique e tente novamente", "Erro", JOptionPane.INFORMATION_MESSAGE);
                return;
            }
            
            Cliente clienteNovo = new Cliente(nome, cpf, telefone, endereco, numero, cidade, estado);
            iClienteDAO.alterar(clienteNovo);
            
            model.removeRow(linhaSelecionada);
            model.addRow(new Object[]{clienteNovo.getNome(), clienteNovo.getCpf(), clienteNovo.getTelefone(), clienteNovo.getEndereco()});
            
            JOptionPane.showMessageDialog(null,
                    "Cliente atualizado com sucesso!",
                    "Sucesso",
                    JOptionPane.INFORMATION_MESSAGE);
            limparCampos();
        }
        else {
            JOptionPane.showMessageDialog(null,
                        "Nenhum cliente selecionado!",
                        "Excluir",
                        JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnAtualizarActionPerformed

